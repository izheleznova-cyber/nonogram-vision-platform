"""
passport_record.py
"""

from __future__ import annotations

from dataclasses import dataclass
from dataclasses import field


@dataclass(slots=True)
class PassportRecord:
    """
    Nonogram passport.
    """

    # -------------------------------------------------------------------------
    # Identification
    # -------------------------------------------------------------------------

    worksheet_name: str
    id: str

    # -------------------------------------------------------------------------
    # Puzzle
    # -------------------------------------------------------------------------

    width: int
    height: int
    pixel_count: int

    # -------------------------------------------------------------------------
    # Classification
    # -------------------------------------------------------------------------

    category: str
    subcategory: str

    # -------------------------------------------------------------------------
    # Names
    # -------------------------------------------------------------------------

    title: str
    synonyms: list[str]
    author_title: str

    # -------------------------------------------------------------------------
    # Source
    # -------------------------------------------------------------------------

    source: str
    url: str
    page_id: int

    # -------------------------------------------------------------------------
    # Recognition
    # -------------------------------------------------------------------------

    difficulty: int
    subject: str
    recognition_level: str

    # -------------------------------------------------------------------------
    # Face
    # -------------------------------------------------------------------------

    has_face: bool
    face_size: str
    orientation: str

    # -------------------------------------------------------------------------
    # Semantics
    # -------------------------------------------------------------------------

    emotion: bool
    context: bool
    color: str

    # -------------------------------------------------------------------------
    # Analysis
    # -------------------------------------------------------------------------

    uncertainty: str

    # -------------------------------------------------------------------------
    # Additional experimental fields
    # -------------------------------------------------------------------------

    extra: dict[str, str] = field(default_factory=dict)